function change() {
    var img = document.getElementById("bulb");
    console.log("Current image source: " + img.src);
    if (img.src.includes("off")) {
        img.src = "./assets/on.jpg";
        console.log("Changed to ON");
    } else {
        img.src = "./assets/off.jpg";
        console.log("Changed to OFF");
    }
}


























































































































































































































































